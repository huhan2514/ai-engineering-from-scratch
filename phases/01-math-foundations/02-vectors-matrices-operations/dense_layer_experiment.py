import numpy as np

# =========================
# 1. 输入向量 x
# =========================
# x 有 3 个输入特征，所以 shape 是 3x1

x = np.array([[0.5],
              [0.8],
              [0.2]])

# =========================
# 2. 权重矩阵 W
# =========================
# W 是 2x3
# 代表：把 3 个输入特征变成 2 个输出特征

W = np.array([[1.0, -1.0, 0.5],
              [0.2,  0.3, -0.4]])

# =========================
# 3. bias 偏置
# =========================
# b 是 2x1，因为输出有 2 个数字

b = np.array([[0.1],
              [0.1]])

# =========================
# 4. ReLU 激活函数
# =========================
# ReLU 的意思：
# 如果数字小于 0，就变成 0
# 如果数字大于 0，就保持原样

def relu(z):
    return np.maximum(0, z)

# =========================
# 5. 前向传播
# =========================

z = W @ x + b
output = relu(z)

print("x shape:", x.shape)
print("W shape:", W.shape)
print("b shape:", b.shape)

print("\nW @ x + b =")
print(z)

print("\nAfter ReLU =")
print(output)